import { describe, expect, test } from "vitest";
import { findOptimalPath } from "./pouringPathFinder";

describe("Water Pouring Function", () => {
  test("(10 , 2 , 4 ) ---> [ [0, 2], [2, 0], [2, 2] [4, 0] ]", () => {
    expect(findOptimalPath(10, 2, 4, true)).toStrictEqual([
      [0, 2],
      [2, 0],
      [2, 2],
      [4, 0],
    ]);
  });

  test("(2, 4, 12 ) ---> 'Not possible' because X and Y are smaller than Z", () => {
    expect(findOptimalPath(2, 4, 12, true)).toStrictEqual(["Not possible"]);
  });

  test("(100, 236, 120) ---> [[0, 100],[100, 100], [0, 200],[100, 200],[64, 236],[64, 0],[0, 64],[100, 64],[0, 164],[100, 164],[28, 236],[28, 0],[0, 28],[100, 28],[0, 128],[100, 128],[0, 228],[100, 228],[92, 236],[92, 0],[0, 92],[100, 92],[0, 192],[100, 192],[56, 236],[56, 0],[0, 56],[100, 56],[0, 156],[100, 156],[20, 236],[20, 0],[0, 20],[100, 20],[0, 120]]", () => {
    expect(findOptimalPath(100, 236, 120, true)).toStrictEqual([
      [100, 0],
      [0, 100],
      [100, 100],
      [0, 200],
      [100, 200],
      [64, 236],
      [64, 0],
      [0, 64],
      [100, 64],
      [0, 164],
      [100, 164],
      [28, 236],
      [28, 0],
      [0, 28],
      [100, 28],
      [0, 128],
      [100, 128],
      [0, 228],
      [100, 228],
      [92, 236],
      [92, 0],
      [0, 92],
      [100, 92],
      [0, 192],
      [100, 192],
      [56, 236],
      [56, 0],
      [0, 56],
      [100, 56],
      [0, 156],
      [100, 156],
      [20, 236],
      [20, 0],
      [0, 20],
      [100, 20],
      [0, 120],
    ]);
  });
  test("(3, 7, 5) Odd numbers ---> [[0,7],[3,4],[0,4],[3,1],[0,1],[1,0],[1,7],[3,5]]", () => {
    expect(findOptimalPath(3, 7, 5, true)).toStrictEqual([
      [0, 7],
      [3, 4],
      [0, 4],
      [3, 1],
      [0, 1],
      [1, 0],
      [1, 7],
      [3, 5],
    ]);
  });
});
